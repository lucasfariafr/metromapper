public class Coloration {
    public int numSommet;
    public String nomSommet;
    public int numeroLigne;
    public Boolean siTerminus;
    public int branchement;

    public Coloration(int numSommet, String nomSommet, int numeroLigne, Boolean siTerminus, int branchement) {
        this.numSommet = numSommet;
        this.nomSommet = nomSommet;
        this.numeroLigne = numeroLigne;
        this.siTerminus = siTerminus;
        this.branchement = branchement;
    }
}
